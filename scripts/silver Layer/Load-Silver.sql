 /*
 EXEC Silver.load_silver;
 */

CREATE OR ALTER PROCEDURE silver.load_silver AS
BEGIN
	PRINT '================================================';
	PRINT 'Loading Silver Layer';
	PRINT '================================================';

	PRINT '------------------------------------------------';
	PRINT 'Loading CRM Tables';
	PRINT '------------------------------------------------';

	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------

	PRINT '>> Truncating Table: silver.crm_cust_info';
	TRUNCATE TABLE silver.crm_cust_info;
	PRINT '>> Inserting Data Into: silver.crm_cust_info';

	INSERT INTO silver.crm_cust_info
	(
		cst_id, 
		cst_key, 
		cst_firstname, 
		cst_lastname, 
		cst_marital_status, 
		cst_gndr,
		cst_create_date
	)
		select 
		cst_id,
		cst_key,
		TRIM(cst_firstname) as cst_firstname,
		TRIM(cst_lastname) as cst_lastname,
		CASE WHEN cst_marital_status = 'S' then 'Single'
  			 WHEN cst_marital_status = 'M' then 'Married'
			 ELSE 'N/A'
		END as cst_marital_status,
		CASE WHEN cst_gndr = 'F' then 'Female'
  			 WHEN cst_gndr = 'M' then 'Male'
			 ELSE 'N/A'
		END as cst_gndr,
		cst_create_date
		from(
			select * , ROW_NUMBER() over (partition by cst_id order by cst_create_date desc) as flag_number
			from bronze.crm_cust_info
			WHERE cst_id IS NOT NULL
			)t
		where flag_number = 1
	
	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------

	PRINT '>> Truncating Table: silver.crm_prd_info';
	TRUNCATE TABLE silver.crm_prd_info;
	PRINT '>> Inserting Data Into: silver.crm_prd_info';
	INSERT INTO silver.crm_prd_info (
		prd_id,
		cat_id,
		prd_key,
		prd_nm,
		prd_cost,
		prd_line,
		prd_start_dt,
		prd_end_dt
	)
	SELECT 
	prd_id,
	REPLACE(SUBSTRING(prd_key,1,5),'-','_') AS cat_id,-- extract and transform category ID form product key to make join easily with erp_px_cat_g1v2
	SUBSTRING(prd_key,7,LEN(prd_key)) AS prd_key ,
	prd_nm,
	ISNULL(prd_cost,0) as prd_cost,
	CASE 
		WHEN UPPER(TRIM(prd_line)) = 'M' THEN 'Mountain'
		WHEN UPPER(TRIM(prd_line)) = 'R' THEN 'Road'
		WHEN UPPER(TRIM(prd_line)) = 'S' THEN 'Other Sales'
		WHEN UPPER(TRIM(prd_line)) = 'T' THEN 'Touring'
		ELSE 'N/A'
	END AS prd_line, -- Map product line codes to descriptive values
	CAST(prd_start_dt AS DATE) AS prd_start_dt,
	CAST(LEAD(prd_start_dt) OVER (PARTITION BY prd_key ORDER BY prd_start_dt) - 1 AS DATE) AS prd_end_dt -- Calculate end date as one day before the next start date
	FROM bronze.crm_prd_info

	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------

	PRINT '>> Truncating Table: silver.crm_sales_details';
	TRUNCATE TABLE silver.crm_sales_details;
	PRINT '>> Inserting Data Into: silver.crm_sales_details';
	INSERT INTO silver.crm_sales_details (
		sls_ord_num,
		sls_prd_key,
		sls_cust_id,
		sls_order_dt,
		sls_ship_dt,
		sls_due_dt,
		sls_sales,
		sls_quantity,
		sls_price
	)
	SELECT 
	sls_ord_num,
	sls_prd_key,
	sls_cust_id,
	CASE 
		WHEN sls_order_dt = 0 OR LEN(sls_order_dt) != 8 THEN NULL
		ELSE CAST(CAST(sls_order_dt AS VARCHAR) AS DATE)
	END AS sls_order_dt,
	CASE 
		WHEN sls_ship_dt = 0 OR LEN(sls_ship_dt) != 8 THEN NULL
		ELSE CAST(CAST(sls_ship_dt AS VARCHAR) AS DATE)
	END AS sls_ship_dt,
	CASE 
		WHEN sls_due_dt = 0 OR LEN(sls_due_dt) != 8 THEN NULL
		ELSE CAST(CAST(sls_due_dt AS VARCHAR) AS DATE)
	END AS sls_due_dt,
	CASE WHEN sls_sales IS NULL OR sls_sales <= 0 or sls_sales != sls_quantity * ABS(sls_price)
		THEN sls_quantity * ABS(sls_price)
		ELSE sls_sales
	end as sls_sales,
	sls_quantity,
	CASE WHEN sls_price IS NULL OR sls_price <= 0 
		THEN sls_sales / NULLIF(sls_quantity, 0)
		ELSE sls_price  
	end as sls_price
	FROM bronze.crm_sales_details

	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------
	PRINT '------------------------------------------------';
	PRINT 'Loading ERP Tables';
	PRINT '------------------------------------------------';
	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------

	PRINT '>> Truncating Table: silver.erp_cust_az12';
	TRUNCATE TABLE silver.erp_cust_az12;
	PRINT '>> Inserting Data Into: silver.erp_cust_az12';
	INSERT INTO silver.erp_cust_az12 (
		cid,
		bdate,
		gen
	)
	SELECT 
	CASE WHEN cid LIKE 'NAS%' then SUBSTRING(cid,4,LEN(cid))
	else cid
	end as cid,
	CASE WHEN bdate > GETDATE() then NULL
	else bdate
	end as bdate,
	CASE WHEN gen IN ('F', 'Female') then 'Female'
  		 WHEN gen IN ('M', 'Male') then 'Male'
		 ELSE 'N/A'
	END as gen
	FROM bronze.erp_cust_az12

	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------

	PRINT '>> Truncating Table: silver.erp_loc_a101';
	TRUNCATE TABLE silver.erp_loc_a101;
	PRINT '>> Inserting Data Into: silver.erp_loc_a101';
	INSERT INTO silver.erp_loc_a101 (
		cid,
		cntry
	)
	SELECT 
	REPLACE(cid,'-','') AS cid,
	CASE
		WHEN TRIM(cntry) = 'DE' THEN 'Germany'
		WHEN TRIM(cntry) IN ('US', 'USA') THEN 'United States'
		WHEN TRIM(cntry) = '' OR cntry IS NULL THEN 'N/A'
		ELSE TRIM(cntry)
	END AS cntry
	FROM bronze.erp_loc_a101

	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------

	PRINT '>> Truncating Table: silver.erp_px_cat_g1v2';
	TRUNCATE TABLE silver.erp_px_cat_g1v2;
	PRINT '>> Inserting Data Into: silver.erp_px_cat_g1v2';
	INSERT INTO silver.erp_px_cat_g1v2 (
		id,
		cat,
		subcat,
		maintenance
	)
	SELECT
		id,
		cat,
		subcat,
		maintenance
	FROM bronze.erp_px_cat_g1v2;


	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------
	PRINT '================================================';
	PRINT 'End of silver layer loading ';
	PRINT '================================================';
	---------------------------------------------------------------------------------
	---------------------------------------------------------------------------------
END